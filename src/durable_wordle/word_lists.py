def get_daily_word(date: datetime.date) -> str:
    """Select a deterministic daily word based on the given date.

    Uses the date's ordinal value as a random seed so every caller
    gets the same word for the same calendar day.

    :param date: The calendar date to pick a word for.
    :returns: An uppercase 5-letter word from the answer list.
    """
    rng = random.Random(date.toordinal())
    return rng.choice(ANSWER_LIST)


def is_valid_guess(word: str) -> bool:
    """Check whether a word is in the valid guesses list.

    :param word: The word to check (any case, normalized to uppercase).
    :returns: ``True`` if the word is a valid 5-letter guess.
    """
    return word.upper() in VALID_GUESSES
